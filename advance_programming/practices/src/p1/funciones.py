def suma(a, b):
    "Suma dos numeros"
    return a+b

def resta(a, b):
    "Resta dos numeros"
    return a-b

def mult(a, b):
    "Multiplica dos numeros"
    return a*b

def div(a, b):
    "Divide dos numeros"
    return a/b
